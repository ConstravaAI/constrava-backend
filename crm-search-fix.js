import fs from "fs";

const target = "server.js";
let text = fs.readFileSync(target, "utf8");

function replaceRegex(pattern, replacement) {
  if (pattern.test(text)) {
    text = text.replace(pattern, replacement);
    return true;
  }
  return false;
}

const helpers = `
function crmPayloadObject(row = {}) {
  const raw = row.payload || row.data || row.meta || row.details || null;
  if (!raw) return {};
  if (typeof raw === "object") return raw;
  if (typeof raw === "string") {
    try {
      const parsed = JSON.parse(raw);
      return parsed && typeof parsed === "object" ? parsed : {};
    } catch (err) {
      return { raw };
    }
  }
  return {};
}

function crmValue(row, names, fallback = "") {
  const payload = crmPayloadObject(row);
  const direct = valueFrom(row, names, "");
  if (direct !== undefined && direct !== null && String(direct).trim() !== "") return direct;
  const nested = valueFrom(payload, names, "");
  if (nested !== undefined && nested !== null && String(nested).trim() !== "") return nested;
  return fallback;
}

function publicLead(row = {}) {
  return {
    name: String(crmValue(row, ["name", "full_name", "lead_name", "contact_name"], "Demo Lead")),
    email: String(crmValue(row, ["email", "lead_email", "contact_email", "email_address"], "")),
    phone: String(crmValue(row, ["phone", "phone_number", "mobile", "cell"], "")),
    company: String(crmValue(row, ["company", "organization", "business", "account", "company_name"], "—")),
    status: String(crmValue(row, ["status", "stage", "lead_status", "pipeline"], "New")),
    source: String(crmValue(row, ["source", "channel", "campaign", "referrer"], "Website")),
    notes: String(crmValue(row, ["notes", "message", "body", "description", "request", "raw"], "")),
    created_at: String(crmValue(row, ["created_at", "timestamp", "received_at", "inserted_at"], "")),
  };
}

function crmSearchScore(lead, query) {
  const q = String(query || "").toLowerCase().trim();
  if (!q) return 1;

  const searchable = [lead.name, lead.email, lead.phone, lead.company, lead.status, lead.source, lead.notes]
    .join(" ")
    .toLowerCase();

  const genericWords = new Set(["lead", "leads", "person", "people", "client", "clients", "customer", "customers", "contact", "contacts", "company", "companies", "business", "businesses", "type", "types", "find", "show", "me"]);
  const words = q.split(/\s+/).map((word) => word.replace(/[^a-z0-9]/g, "")).filter((word) => word && !genericWords.has(word));

  let score = 0;
  if (searchable.includes(q)) score += 100;

  for (const word of words) {
    if (searchable.includes(word)) score += 18;
  }

  const industryGroups = [
    { keys: ["manufacturing", "factory", "industrial", "plant", "machine", "machining", "production", "warehouse"], terms: ["manufacturing", "factory", "industrial", "plant", "machine", "machining", "production", "warehouse", "hvac", "forge", "fabrication", "maintenance"] },
    { keys: ["fitness", "gym", "trainer", "sports", "athlete"], terms: ["fitness", "gym", "trainer", "sports", "athlete", "wellness", "boxing", "martial"] },
    { keys: ["design", "studio", "creative", "agency", "brand"], terms: ["design", "studio", "creative", "agency", "brand", "marketing", "media"] },
    { keys: ["tech", "software", "saas", "ai", "data", "lab"], terms: ["tech", "software", "saas", "ai", "data", "lab", "labs", "startup", "app"] },
    { keys: ["service", "contractor", "local", "home", "construction"], terms: ["service", "contractor", "local", "home", "construction", "hvac", "plumbing", "roofing", "repair"] }
  ];

  for (const group of industryGroups) {
    if (group.keys.some((key) => q.includes(key))) {
      if (group.terms.some((term) => searchable.includes(term))) score += 75;
    }
  }

  const status = String(lead.status || "").toLowerCase();
  const source = String(lead.source || "").toLowerCase();
  const notes = String(lead.notes || "").toLowerCase();

  if (/ready|buy|buyer|buyers|hot|serious|qualified|proposal|close|decision|urgent|high.intent|best/.test(q)) {
    if (/qualified|proposal|won|contacted/.test(status)) score += 45;
    if (/pricing|contact|referral|search/.test(source)) score += 20;
    if (/budget|timeline|quote|proposal|urgent|ready|buy|purchase/.test(notes)) score += 25;
  }

  if (/new|uncontacted|fresh/.test(q) && /new/.test(status)) score += 45;
  if (/proposal|quote|estimate/.test(q) && /proposal/.test(status + " " + notes)) score += 45;
  if (/won|closed/.test(q) && /won/.test(status)) score += 45;
  if (/lost|dead|bad.fit/.test(q) && /lost/.test(status)) score += 45;

  return score;
}
`;

// Replace the weaker publicLead + crmSearchScore block generated by crm-real-upgrade.js.
replaceRegex(/function publicLead\(row = \{\}\) \{[\s\S]*?function importedCell\(row, names\) \{/, helpers + "\nfunction importedCell(row, names) {");

const searchRoute = `app.get("/crm/search", async (req, res) => {
  try {
    const ctx = await crmContext(req);
    if (ctx.error) return res.status(ctx.status).json({ ok: false, error: ctx.error });

    const qText = String(req.query.q || "").trim();
    const rawLeads = await getCrmLeads(ctx.siteId, 250);
    let sourceLeads = rawLeads.map(publicLead);

    // Important for the public demo: demo leads are generated inside dashboardJson,
    // not always stored in crm_leads. Fall back to the dashboard payload so searches
    // like "manufacturing leads" can still find Lee Manufacturing.
    if (!sourceLeads.length) {
      const payload = await getDashboardPayload(ctx.token);
      sourceLeads = (payload && payload.leads ? payload.leads : []).map(publicLead);
    }

    const ranked = sourceLeads
      .map((lead) => ({ ...lead, score: crmSearchScore(lead, qText) }))
      .filter((lead) => !qText || lead.score > 0)
      .sort((a, b) => b.score - a.score)
      .slice(0, 75)
      .map(({ score, ...lead }) => lead);

    res.json({ ok: true, query: qText, leads: ranked, count: ranked.length });
  } catch (err) {
    console.error("CRM SEARCH ERROR:", err);
    res.status(500).json({ ok: false, error: err.message });
  }
});`;

replaceRegex(/app\.get\("\/crm\/search", async \(req, res\) => \{[\s\S]*?\n\}\);\n\napp\.post\("\/crm\/leads"/, searchRoute + "\n\napp.post(\"/crm/leads\"");

fs.writeFileSync(target, text);
console.log("Upgraded CRM search to support industry/type queries and demo fallback leads.");
